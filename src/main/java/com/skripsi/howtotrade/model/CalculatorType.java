package com.skripsi.howtotrade.model;

public class CalculatorType {
    private int calculatorTypeId;
    private String calculatorTypeName;

    public int getCalculatorTypeId() {
        return this.calculatorTypeId;
    }

    public void setCalculatorTypeId(int calculatorTypeId) {
        this.calculatorTypeId = calculatorTypeId;
    }

    public String getCalculatorTypeName() {
        return this.calculatorTypeName;
    }

    public void setCalculatorTypeName(String calculatorTypeName) {
        this.calculatorTypeName = calculatorTypeName;
    }
    
}
